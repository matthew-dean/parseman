/**
 * Emit browser parsers for Chevrotain's JSON benchmark suite.
 *
 * Usage:
 *   node --import tsx/esm scripts/build-chevrotain-bench.mjs [output.js | outputDir]
 *
 * - output.js  — compiled parser only (stdout if omitted)
 * - outputDir  — writes parseman_compiled_parser.js and parseman_interpreter_parser.js
 */
import { writeFileSync, readFileSync, mkdirSync } from 'node:fs'
import { dirname, join, extname } from 'node:path'
import { fileURLToPath } from 'node:url'
import * as esbuild from 'esbuild'
import { compile } from '../src/index.ts'
import { jsonRecognize } from '../examples/json/chevrotain-bench.ts'

const rootDir = fileURLToPath(new URL('..', import.meta.url))
const { version } = JSON.parse(readFileSync(new URL('../package.json', import.meta.url), 'utf8'))

function emitCompiled() {
  const compiled = compile(jsonRecognize)
  const mfCount = new Set(
    [...compiled.source.matchAll(/_mf\[(\d+)\]/g)].map(m => Number(m[1])),
  ).size

  const voidFn = 'function(_v,_s){return undefined}'
  const mfDecl = `const _mf = [${Array.from({ length: mfCount }, () => voidFn).join(', ')}]`

  return {
    source: `// Generated by Parséman ${version} (compiled) — do not edit by hand.
// Source: examples/json/chevrotain-bench.ts
// Rebuild: pnpm build:chevrotain-bench

${compiled.source.replace(/^function _parse/m, `${mfDecl}\n\nfunction _parse`)}

window.parse = function parse_json_parseman_compiled(input) {
  const result = _parse(input, 0, [], _mf, [], { trackLines: false })
  if (!result.ok) {
    throw new SyntaxError(
      'JSON parse error at offset ' + result.span.start +
      ': expected ' + result.expected.join(' or '),
    )
  }
  return result
}
`,
    mfCount,
  }
}

async function emitInterpreted() {
  const result = await esbuild.build({
    entryPoints: [join(rootDir, 'scripts/chevrotain-bench-interpreter-entry.ts')],
    bundle: true,
    format: 'iife',
    platform: 'browser',
    write: false,
    logLevel: 'silent',
  })

  const banner = `// Generated by Parséman ${version} (interpreted) — do not edit by hand.
// Source: examples/json/chevrotain-bench.ts + scripts/chevrotain-bench-interpreter-entry.ts
// Rebuild: pnpm build:chevrotain-bench

`
  return banner + result.outputFiles[0].text
}

function writeCompiled(target, source, mfCount) {
  mkdirSync(dirname(target), { recursive: true })
  writeFileSync(target, source)
  console.error(`Wrote ${target} (${source.length} bytes, ${mfCount} map fns)`)
}

async function writeInterpreted(target, source) {
  mkdirSync(dirname(target), { recursive: true })
  writeFileSync(target, source)
  console.error(`Wrote ${target} (${source.length} bytes, interpreter bundle)`)
}

const target = process.argv[2]
const { source: compiledSource, mfCount } = emitCompiled()

if (!target) {
  process.stdout.write(compiledSource)
} else if (extname(target) === '.js') {
  writeCompiled(target, compiledSource, mfCount)
} else {
  const compiledPath = join(target, 'parseman_compiled_parser.js')
  const interpretedPath = join(target, 'parseman_interpreter_parser.js')
  writeCompiled(compiledPath, compiledSource, mfCount)
  await writeInterpreted(interpretedPath, await emitInterpreted())
}
