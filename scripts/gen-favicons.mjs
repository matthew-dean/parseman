/**
 * Generate the full favicon set for the docs site from a single source image.
 *
 * Reads assets/favicon.png and emits every platform-specific icon (favicon.ico,
 * 16/32/48 PNGs, Apple touch icon, Android/Chrome maskable icons, Windows tiles)
 * plus the web manifest and browserconfig into docs/public/ — VitePress serves
 * that directory at the site root. The matching <link>/<meta> tags are written
 * to docs/.vitepress/head-icons.ts as VitePress HeadConfig tuples, so the config
 * stays in sync automatically on every re-run.
 *
 * Run: pnpm docs:favicons
 */
import { favicons } from 'favicons'
import { writeFile, mkdir, rm, readdir } from 'node:fs/promises'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const SOURCE = join(root, 'assets', 'favicon.png')
const OUT_DIR = join(root, 'docs', 'public')
const HEAD_MODULE = join(root, 'docs', '.vitepress', 'head-icons.ts')

// GitHub Pages project site — every href needs the /parseman/ base prefix.
const BASE = '/parseman/'

/** Files this script owns in docs/public — cleaned before each run. */
const OWNED = /^(favicon.*\.(ico|png|svg)|apple-touch-icon.*\.png|android-chrome-.*\.png|mstile-.*\.png|manifest\.webmanifest|browserconfig\.xml)$/

const configuration = {
  path: BASE,
  appName: 'Parséman',
  appShortName: 'Parséman',
  appDescription:
    'Parser combinators that compile to optimized JavaScript — use as a library or as a build-time macro.',
  background: '#ffffff',
  theme_color: '#ce2b37',
  display: 'standalone',
  start_url: BASE,
  icons: {
    // Browser tab icons keep the source transparency.
    favicons: true,
    android: true,
    // Apple/Windows expect opaque icons — transparent areas render as black
    // on the iOS home screen, so flatten onto white.
    appleIcon: { background: '#ffffff', transparent: false },
    windows: { background: '#ffffff', transparent: false },
    appleStartup: false,
    yandex: false,
  },
}

/** Parse a favicons-emitted HTML tag string into a VitePress HeadConfig tuple. */
function toHeadTuple(html) {
  const tag = html.match(/^<([a-zA-Z]+)/)?.[1]
  if (!tag) return null
  const attrs = {}
  for (const [, key, value] of html.matchAll(/([a-zA-Z][\w-]*)="([^"]*)"/g)) {
    attrs[key] = value
  }
  return [tag, attrs]
}

async function main() {
  // Clean previously generated icons so removed sizes don't linger.
  await mkdir(OUT_DIR, { recursive: true })
  for (const name of await readdir(OUT_DIR)) {
    if (OWNED.test(name)) await rm(join(OUT_DIR, name))
  }

  const response = await favicons(SOURCE, configuration)

  await Promise.all(
    response.images.map((img) => writeFile(join(OUT_DIR, img.name), img.contents)),
  )
  await Promise.all(
    response.files.map((file) => writeFile(join(OUT_DIR, file.name), file.contents)),
  )

  const head = response.html
    .map(toHeadTuple)
    .filter((t) => t !== null)

  const banner =
    '// AUTO-GENERATED by scripts/gen-favicons.mjs — do not edit.\n' +
    '// Regenerate with: pnpm docs:favicons\n' +
    "import type { HeadConfig } from 'vitepress'\n\n"
  await writeFile(
    HEAD_MODULE,
    banner + 'export const faviconHead: HeadConfig[] = ' + JSON.stringify(head, null, 2) + '\n',
  )

  console.log(
    `favicons: wrote ${response.images.length} images + ${response.files.length} files to docs/public/, ` +
      `${head.length} head tags to docs/.vitepress/head-icons.ts`,
  )
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
